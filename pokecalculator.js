function typeCalculator(attack, types) {
    let total = 1;
    
    for (let type of types) {
        let atk = typeNames.indexOf(attack);
        let dfd = typeNames.indexOf(type);
        let dfIndex = typeBase[atk][dfd];
        
        if (dfIndex === 0) {
            return 0;
        } else {
            total *= dfIndex;
        }
    }
    
    return total;
}
